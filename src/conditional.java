public class conditional {
    public static void main(String[] args) {
        int num = 0;

        if (num > 0) {
            System.out.println("num은 0보다 크다.");
        } else if (num < 0) {
            System.out.println("num은 0보다 작다.");
        } else {
            System.out.println("num은 0이다.");
        }
    }
}
